  void _toggleContactSelection(ContactModel contact) {
    setState(() {
      if (_selectedContacts.contains(contact)) {
        _selectedContacts.remove(contact);
        print('âŒ Deselected: ${contact.name}');
      } else {
        _selectedContacts.add(contact);
        print('âœ… Selected: ${contact.name} (${contact.phoneNumber})');
      }
      print('ðŸ“Š Total selected: ${_selectedContacts.length}');
    });
  }

  void _sendMessage() async {
    print('ðŸ”„ Starting message send process...');
    print('ðŸ‘¥ Selected contacts: ${_selectedContacts.length}');

    if (_selectedContacts.isEmpty) {
      print('âŒ No contacts selected');
      Fluttertoast.showToast(msg: 'Please select at least one recipient');
      return;
    }

    if (_messageController.text.trim().isEmpty) {
      print('âŒ No message entered');
      Fluttertoast.showToast(msg: 'Please enter a message');
      return;
    }

    setState(() => _isLoading = true);

    try {
      int successCount = 0;
      int failCount = 0;

      print('ðŸ“¤ Sending to ${_selectedContacts.length} contacts...');
      for (final contact in _selectedContacts) {
        print('ðŸ“ž Sending to: ${contact.name} (${contact.phoneNumber})');
        final result = await SemaphoreService.sendSMS(
          contact.phoneNumber,
          _messageController.text.trim(),
          senderName: _senderNameController.text.trim().isNotEmpty
              ? _senderNameController.text.trim()
              : null,
        );

        // Save message history regardless of success/failure
        final messageHistory = MessageHistory(
          id: '', // Will be generated by the bloc
          senderName: _senderNameController.text.trim().isNotEmpty
              ? _senderNameController.text.trim()
              : 'System',
          message: _messageController.text.trim(),
          receiverName: contact.name,
          receiverPhone: contact.phoneNumber,
          timestamp: DateTime.now(),
          sentSuccessfully: result,
        );

        context.read<MessageHistoryBloc>().add(
          AddMessageHistoryEvent(messageHistory: messageHistory),
        );

        if (result) {
          successCount++;
        } else {
          failCount++;
        }
      }

      if (successCount > 0) {
        Fluttertoast.showToast(
          msg:
              'Message sent to $successCount recipient${successCount > 1 ? 's' : ''}',
          backgroundColor: Colors.green,
        );
      }

      if (failCount > 0) {
        Fluttertoast.showToast(
          msg:
              'Failed to send to $failCount recipient${failCount > 1 ? 's' : ''}',
          backgroundColor: Colors.red,
        );
      }

      // Clear message after sending
      _messageController.clear();
      setState(() => _selectedContacts.clear());
    } catch (e) {
      Fluttertoast.showToast(
        msg: 'Error sending messages: $e',
        backgroundColor: Colors.red,
      );
    } finally {
      setState(() => _isLoading = false);
    }
  }

